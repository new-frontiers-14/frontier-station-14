using System.Collections.Generic;
using Content.Shared._NF.Library;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Localization;

namespace Content.Client._NF.Library.AdminUI;

[GenerateTypedNameReferences]
public sealed partial class AdminLibraryWindow : DefaultWindow
{
    public event Action<int>? OnDeleteBook;

    private int _selectedBookIndex = -1;
    private readonly List<int> _bookIds = new();
    private readonly List<AdminLibraryBookEntry> _allBooks = new();

    public AdminLibraryWindow()
    {
        RobustXamlLoader.Load(this);

        BookList.OnItemSelected += args =>
        {
            _selectedBookIndex = args.ItemIndex;
            UpdatePreview();
            UpdateDeleteButtonState();
        };
        BookList.OnItemDeselected += _ =>
        {
            _selectedBookIndex = -1;
            UpdatePreview();
            UpdateDeleteButtonState();
        };

        SearchEdit.OnTextChanged += _ => FilterBookList();

        DeleteButton.OnPressed += _ =>
        {
            if (_selectedBookIndex < 0 || _selectedBookIndex >= _bookIds.Count)
                return;
            OnDeleteBook?.Invoke(_bookIds[_selectedBookIndex]);
        };
    }

    /// <summary>
    /// Populates the window's book list with the provided entries from the server.
    /// </summary>
    public void PopulateBooks(List<AdminLibraryBookEntry> books)
    {
        _allBooks.Clear();
        _allBooks.AddRange(books);
        FilterBookList();
    }

    /// <summary>
    /// Rebuilds the list applying the current search filter.
    /// </summary>
    private void FilterBookList()
    {
        BookList.Clear();
        _bookIds.Clear();
        _selectedBookIndex = -1;
        UpdatePreview();

        var query = SearchEdit.Text.Trim();

        var filtered = string.IsNullOrEmpty(query)
            ? _allBooks
            : _allBooks.FindAll(b =>
                b.Title.Contains(query, StringComparison.OrdinalIgnoreCase) ||
                b.Author.Contains(query, StringComparison.OrdinalIgnoreCase) ||
                b.AuthorCKey.Contains(query, StringComparison.OrdinalIgnoreCase));

        EmptyLabel.Visible = filtered.Count == 0;

        foreach (var book in filtered)
        {
            BookList.AddItem($"{book.Title}  —  {book.Author}  |  {book.Date}  |  {book.AuthorCKey}");
            _bookIds.Add(book.Id);
        }

        UpdateDeleteButtonState();
    }

    /// <summary>
    /// Updates the detail preview panel to show the currently selected book.
    /// </summary>
    private void UpdatePreview()
    {
        if (_selectedBookIndex < 0 || _selectedBookIndex >= _bookIds.Count)
        {
            PreviewPanel.Visible = false;
            return;
        }

        var id = _bookIds[_selectedBookIndex];
        var book = _allBooks.Find(b => b.Id == id);
        if (book == null)
        {
            PreviewPanel.Visible = false;
            return;
        }

        PreviewTitle.Text = Loc.GetString("library-admin-preview-title", ("title", book.Title));
        PreviewAuthor.Text = Loc.GetString("library-admin-preview-author", ("author", book.Author));
        PreviewCKey.Text = Loc.GetString("library-admin-preview-ckey", ("ckey", book.AuthorCKey));
        PreviewDate.Text = Loc.GetString("library-admin-preview-date", ("date", book.Date));
        PreviewContent.Text = book.Content;
        PreviewPanel.Visible = true;
    }

    private void UpdateDeleteButtonState()
    {
        DeleteButton.Disabled = _selectedBookIndex < 0 || _selectedBookIndex >= _bookIds.Count;
    }
}
