using System.Collections.Generic;
using Content.Client.Paper.UI;
using Content.Shared._NF.Library;
using Content.Shared.Paper;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Localization;

namespace Content.Client._NF.Library.AdminUI;

[GenerateTypedNameReferences]
public sealed partial class AdminLibraryWindow : DefaultWindow
{
    public event Action<int>? OnDeleteBook;

    private PaperWindow? _bookViewWindow;
    private int? _selectedBookId;
    private readonly List<AdminLibraryBookEntry> _allBooks = new();

    public AdminLibraryWindow()
    {
        RobustXamlLoader.Load(this);

        BookList.OnItemSelected += args =>
        {
            if (args.ItemList[args.ItemIndex].Metadata is int bookId)
                _selectedBookId = bookId;
            else
                _selectedBookId = null;

            UpdatePreview();
            UpdateButtonState();
        };
        BookList.OnItemDeselected += _ =>
        {
            _selectedBookId = null;
            UpdatePreview();
            UpdateButtonState();
        };

        SearchEdit.OnTextChanged += _ => FilterBookList();

        OpenBookViewButton.OnPressed += _ => OpenBookView();

        DeleteButton.OnPressed += _ =>
        {
            if (_selectedBookId is not { } bookId)
                return;

            OnDeleteBook?.Invoke(bookId);
        };

        OnClose += () => _bookViewWindow?.Close();
    }

    /// <summary>
    /// Populates the window's book list with the provided entries from the server.
    /// </summary>
    public void PopulateBooks(List<AdminLibraryBookEntry> books)
    {
        _allBooks.Clear();
        _allBooks.AddRange(books);
        FilterBookList();
    }

    /// <summary>
    /// Rebuilds the list applying the current search filter.
    /// </summary>
    private void FilterBookList()
    {
        BookList.Clear();
        _selectedBookId = null;
        UpdatePreview();

        var query = SearchEdit.Text.Trim();

        var filtered = string.IsNullOrEmpty(query)
            ? _allBooks
            : _allBooks.FindAll(b =>
                b.Title.Contains(query, StringComparison.OrdinalIgnoreCase) ||
                b.Author.Contains(query, StringComparison.OrdinalIgnoreCase) ||
                b.AuthorPlayerUserId.ToString().Contains(query, StringComparison.OrdinalIgnoreCase));

        EmptyLabel.Visible = filtered.Count == 0;

        foreach (var book in filtered)
        {
            BookList.AddItem($"{book.Title}  —  {book.Author}  |  {book.Date}  |  {book.AuthorPlayerUserId}", metadata: book.Id);
        }

        UpdateButtonState();
    }

    /// <summary>
    /// Updates the detail preview panel to show the currently selected book.
    /// </summary>
    private void UpdatePreview()
    {
        if (_selectedBookId is not { } selectedBookId)
        {
            PreviewPanel.Visible = false;
            return;
        }

        var book = _allBooks.Find(b => b.Id == selectedBookId);
        if (book == null)
        {
            PreviewPanel.Visible = false;
            return;
        }

        PreviewTitle.Text = Loc.GetString("library-admin-preview-title", ("title", book.Title));
        PreviewAuthor.Text = Loc.GetString("library-admin-preview-author", ("author", book.Author));
        PreviewPlayerUserId.Text = Loc.GetString("library-admin-preview-player-user-id", ("playerUserId", book.AuthorPlayerUserId));
        PreviewDate.Text = Loc.GetString("library-admin-preview-date", ("date", book.Date));
        PreviewContent.Text = book.Content;
        PreviewPanel.Visible = true;
    }

    private void UpdateButtonState()
    {
        var hasSelection = _selectedBookId.HasValue;
        DeleteButton.Disabled = !hasSelection;
        OpenBookViewButton.Disabled = !hasSelection;
    }

    private void OpenBookView()
    {
        var book = GetSelectedBook();
        if (book == null)
            return;

        _bookViewWindow?.Close();

        _bookViewWindow = new PaperWindow();
        _bookViewWindow.Populate(new PaperComponent.PaperBoundUserInterfaceState(book.Content, new(), PaperComponent.PaperAction.Read));
        _bookViewWindow.OpenCentered();
    }

    private AdminLibraryBookEntry? GetSelectedBook()
    {
        if (_selectedBookId is not { } selectedBookId)
            return null;

        return _allBooks.Find(b => b.Id == selectedBookId);
    }
}
