using Content.Client.UserInterface.Controls;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.XAML;
using Content.Shared._NF.Radio;

namespace Content.Client._NF.Radio.UI;

[GenerateTypedNameReferences]
public sealed partial class NFHandheldRadioMenu : FancyWindow
{

    public event Action<HandheldRadioMode>? OnMicPressed;
    public event Action<HandheldRadioMode>? OnSpeakerPressed;
    public event Action<string>? OnFrequencyChanged;

    public NFHandheldRadioMenu()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        MicButtonOff.OnPressed += args => SetMicMode(HandheldRadioMode.Off);
        MicButtonOn.OnPressed += args => SetMicMode(HandheldRadioMode.Private);
        MicButtonIntercom.OnPressed += args => SetMicMode(HandheldRadioMode.Intercom);

        SpeakerButtonOff.OnPressed += args => SetSpeakerMode(HandheldRadioMode.Off);
        SpeakerButtonOn.OnPressed += args => SetSpeakerMode(HandheldRadioMode.Private);
        SpeakerButtonIntercom.OnPressed += args => SetSpeakerMode(HandheldRadioMode.Intercom);

        FrequencyLineEdit.OnTextEntered += e => OnFrequencyChanged?.Invoke(e.Text);
        FrequencyLineEdit.OnFocusExit += e => OnFrequencyChanged?.Invoke(e.Text);
    }

    private void SetMicMode(HandheldRadioMode mode)
    {
        OnMicPressed?.Invoke(mode);

        MicButtonOff.Pressed = mode == HandheldRadioMode.Off;
        MicButtonOn.Pressed = mode == HandheldRadioMode.Private;
        MicButtonIntercom.Pressed = mode == HandheldRadioMode.Intercom;
    }

    private void SetSpeakerMode(HandheldRadioMode mode)
    {
        OnSpeakerPressed?.Invoke(mode);

        SpeakerButtonOff.Pressed = mode == HandheldRadioMode.Off;
        SpeakerButtonOn.Pressed = mode == HandheldRadioMode.Private;
        SpeakerButtonIntercom.Pressed = mode == HandheldRadioMode.Intercom;
    }

    public void Update(HandheldRadioBoundUIState state)
    {
        MicButtonOff.Pressed = state.MicMode == HandheldRadioMode.Off;
        MicButtonOn.Pressed = state.MicMode == HandheldRadioMode.Private;
        MicButtonIntercom.Pressed = state.MicMode == HandheldRadioMode.Intercom;

        SpeakerButtonOff.Pressed = state.SpeakerMode == HandheldRadioMode.Off;
        SpeakerButtonOn.Pressed = state.SpeakerMode == HandheldRadioMode.Private;
        SpeakerButtonIntercom.Pressed = state.SpeakerMode == HandheldRadioMode.Intercom;

        FrequencyLineEdit.Text = state.Frequency.ToString();
    }
}
