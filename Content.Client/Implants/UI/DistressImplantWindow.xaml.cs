using Content.Client.UserInterface.Controls;
using Content.Shared.Implants.Components;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Implants.UI;

[GenerateTypedNameReferences]
public sealed partial class DistressImplantWindow : FancyWindow
{
    public Action<SwanSongOutputMode>? OnModeChanged;
    public Action<string>? OnMessageSubmitted;

    private bool _updatingState;

    public DistressImplantWindow()
    {
        RobustXamlLoader.Load(this);

        ChannelSelector.AddItem("Local", (int) SwanSongOutputMode.Local);
        ChannelSelector.AddItem("Common", (int) SwanSongOutputMode.Common);
        ChannelSelector.AddItem("Medical", (int) SwanSongOutputMode.Medical);

        ChannelSelector.OnItemSelected += args =>
        {
            ChannelSelector.SelectId(args.Id);
            if (_updatingState)
                return;

            OnModeChanged?.Invoke((SwanSongOutputMode) args.Id);
        };

        SaveButton.OnPressed += _ => OnMessageSubmitted?.Invoke(MessageEdit.Text);
    }

    public void UpdateState(string message, SwanSongOutputMode mode)
    {
        _updatingState = true;
        MessageEdit.Text = message;
        ChannelSelector.SelectId((int) mode);
        _updatingState = false;
    }
}
